    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int resp = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro?", "Grabar", JOptionPane.YES_NO_OPTION);
        if (resp == 0) {
            try {

                String[] arrayRoles = cbxRoles.getSelectedItem().toString().split("-");
                int id_rol = Integer.parseInt(arrayRoles[0]);
                new Registro().registrarUnidadInterna(id_rol, txtName.getText());

                resetearTabla();
                limpiarFormulario();
                JOptionPane.showMessageDialog(null, "Grabado exitosamente.", "Grabar", JOptionPane.PLAIN_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Ingrese datos correspondientes", "Grabar", JOptionPane.PLAIN_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void tblUnidadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUnidadMouseClicked
        int seleccion = tblUnidad.rowAtPoint(evt.getPoint());
        int id = Integer.parseInt(String.valueOf(tblUnidad.getValueAt(seleccion, 0)));

        idGeneral = id;
        //btnBorrar.setEnabled(true);

        UnidadInternaDTO unidad = new UnidadInternaDAO().UnidadPorID(id);
        txtName.setText(unidad.getNombre());

        new Lista().listarRoles().stream().filter((rol) -> (rol.getId() == unidad.getIdRol())).forEachOrdered((rol) -> {
            cbxRoles.setSelectedItem(rol.toString());
        });

    }//GEN-LAST:event_tblUnidadMouseClicked


