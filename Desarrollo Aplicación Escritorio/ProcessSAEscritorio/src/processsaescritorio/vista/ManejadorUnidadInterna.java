    public void listarUnidadesInternas() {
        listaUnidad = new Lista().listarUnidadInterna();
        String[] columnas = {"ID", "Nombre", "Rol"};
        DefaultTableModel modeloTabla = new DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int filas, int columnas) {
                return false;
            }
        };
        for (UnidadInternaDTO unidad : listaUnidad) {
            String id = String.valueOf(unidad.getId());
            String name = String.valueOf(unidad.getNombre());
            String address = String.valueOf(unidad.getIdRol());
            Object[] elemento = {id, name, address};
            modeloTabla.addRow(elemento);
        };
        tblUnidad.setModel(modeloTabla);
    }

    public void listadoRoles() {

        for (RolDTO rol : new Lista().listarRol()) {
            //poner reglas de negocio
            cbxRoles.addItem(rol.getId() + "-" + rol.getName());
        }
    }

